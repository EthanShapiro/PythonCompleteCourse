# Using hex() you can convert a number to hexadecimal
print(hex(22))

# Using bin() you can convert numbers to binary
print(bin(226))

# Using pow() you can raise a number to a power
print(pow(2, 4))

# Use abs() to get the absolute value
print(abs(-2))

# Use round() to round a float or decimal to a certain decimal place
print(round(3.4523465))
print(round(3.456345, 2))
